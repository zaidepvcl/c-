#include <stdio.h>

int main() {
    // Khai bao và khoi tao hai bien so nguyen
    int a = 6; // Bien a duoc khoi tao voi giá tri bang 5
    int b = 50; // Bien b duoc khoi tao voi giá tri 10

    // Khai bao bien de luu ket qua tong
    int sum;

    // Tính tong cua a và b
    sum = a + b;

    // Hien thi ket qua
    printf("Tong cua %d và %d là: %d\n", a, b, sum);

    return 0; // Ket thúc chuong trình
}
